export function pickOpening(character) {
  const fallbackByScenario = {
    termination: TERM_FALLBACK,
    retention: RET_FALLBACK,
    "candidate-comp": CAND_FALLBACK,
    "mock-interview": INTERVIEW_FALLBACK,
  };
  const pool = BY_ID[character.id] || fallbackByScenario[character.scenarioId] || RET_FALLBACK;
  return pool[Math.floor(Math.random() * pool.length)];
}
